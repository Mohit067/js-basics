// Truthy and falsy value in javascript.

// AND 
// here are two conditions
// 1. -->  if first is true then return second value
// 2. -->  if first is false then return first value


console.log(10 && 30);  // return 30

console.log(0 && 30); // return 0

console.log(4 && null); // return null


// OR 
// here are two conditions
// 1. -->  if first is true then return first value
// 2. -->  if first is false then return second value


console.log(10 || 30);  // return 10

console.log(0 || 30); // return 30

console.log(4 || null); // return 4