let d = new Date();
console.log(d);

console.log(d.getDate()); // to  provide only date
console.log(d.getMonth()); // to provide only month
console.log(d.getFullYear()); // to provide only year
console.log(d.getDay()); // to provide only day
console.log(d.getHours()); // to provide only hour
console.log(d.getMinutes()); // to provide only minute
console.log(d.getSeconds()); // to provide only second
console.log(d.getMilliseconds()); // to provide only millisecond


